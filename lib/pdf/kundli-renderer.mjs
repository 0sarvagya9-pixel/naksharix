function page(lang, pdfType, title, children) {
  return h(Page, { key: `${safeText(title, lang)}-${Math.random()}`, size: "A4", style: styles.page }, [
    pdfType === "FREE" ? text(lang, t(lang, "watermark"), styles.watermark, { fixed: true, key: "watermark" }) : null,
    h(View, { key: "header", fixed: true, style: styles.header }, [
      h(View, { key: "brand-block" }, [
        text(lang, "Naksharix", styles.brand, { key: "brand" }),
        text(lang, pdfType === "FREE" ? t(lang, "freeReport") : t(lang, "title"), styles.subtitle, { key: "subtitle" })
      ]),
      text(lang, title, styles.pageTitle, { key: "title" })
    ]),
    ...children,
    h(View, { key: "footer", fixed: true, style: styles.footer }, [
      text(lang, "naksharix.com", undefined, { key: "site" }),
      h(Text, { key: "pages", render: ({ pageNumber, totalPages }) => `Page ${pageNumber} / ${totalPages}` })
    ])
  ]);
}

