function planetLabels(house) {
  const planets = Array.isArray(house?.planets) ? house.planets : [];
  return planets.map((planet) => shortPlanet(typeof planet === "string" ? planet : planet?.planet)).filter(Boolean);
}

function shortPlanet(planet) {
  const raw = safeText(planet, "en");
  const map = { Sun: "Su", Moon: "Mo", Mars: "Ma", Mercury: "Me", Jupiter: "Ju", Venus: "Ve", Saturn: "Sa", Rahu: "Ra", Ketu: "Ke", Ascendant: "As", Lagna: "As" };
  return map[raw] ?? raw.slice(0, 3);
}

function signToNumber(sign) {
  const signs = ["Aries", "Taurus", "Gemini", "Cancer", "Leo", "Virgo", "Libra", "Scorpio", "Sagittarius", "Capricorn", "Aquarius", "Pisces"];
  const index = signs.findIndex((item) => item.toLowerCase() === String(sign ?? "").toLowerCase());
  return index >= 0 ? String(index + 1) : undefined;
}

function formatCoordinates(birthDetails) {
  if (!birthDetails) return undefined;
  const latitude = Number(birthDetails.latitude);
  const longitude = Number(birthDetails.longitude);
  if (!Number.isFinite(latitude) || !Number.isFinite(longitude)) return undefined;
  return `${Math.abs(latitude).toFixed(2)}° ${latitude >= 0 ? "N" : "S"} / ${Math.abs(longitude).toFixed(2)}° ${longitude >= 0 ? "E" : "W"}`;
}

function safeText(value, lang = "hi") {
  if (value === null || value === undefined || value === "") return na(lang);
  if (typeof value === "string" || typeof value === "number" || typeof value === "boolean") return String(value);
  if (Array.isArray(value)) return value.map((item) => safeText(item, lang)).join(", ");
  if (typeof value === "object") {
    if ("name" in value) return safeText(value.name, lang);
    if ("label" in value) return safeText(value.label, lang);
    if ("value" in value) return safeText(value.value, lang);
    if ("sign" in value) return safeText(value.sign, lang);
    if ("house" in value) return safeText(value.house, lang);
    if ("planet" in value) return safeText(value.planet, lang);
    return JSON.stringify(value);
  }
  return String(value);
}

function t(lang, key) {
  return LABELS[lang]?.[key] ?? LABELS.en[key] ?? key;
}

function na(lang) {
  return NA[lang] ?? NA.en;
}

function normalizeLanguage(language) {
  return language === "en" || language === "hi" || language === "hinglish" ? language : "hi";
}

function streamToBuffer(stream) {
  return new Promise((resolve, reject) => {
    const chunks = [];
    stream.on("data", (chunk) => chunks.push(Buffer.isBuffer(chunk) ? chunk : Buffer.from(chunk)));
    stream.on("end", () => resolve(Buffer.concat(chunks)));
    stream.on("error", reject);
  });
}
