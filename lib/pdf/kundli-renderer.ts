function planetInterpretationBlocks(planets: unknown[], lang: Locale) {
  const rows = Array.isArray(planets) ? planets.map(toPlanetRow) : [];
  const byPlanet = new Map(rows.map((planet) => [String(planet.planet ?? ""), planet]));
  const order = ["Sun", "Moon", "Mars", "Mercury", "Jupiter", "Venus", "Saturn", "Rahu", "Ketu"];
  return order.flatMap((planetName) => {
    const planet = byPlanet.get(planetName) ?? { planet: planetName };
    const translatedPlanet = translatePlanetValue(planet.planet, lang);
    const sign = translateSignValue(planet.sign, lang);
    const house = safeText(planet.house, lang);
    const nakshatra = translateByCategory(planet.nakshatra, lang, "nakshatra");
    return planetCopy(lang, planetName, translatedPlanet, sign, house, nakshatra);
  });
}

