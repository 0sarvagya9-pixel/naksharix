    row(labels[lang].paksha, translateByCategory(data.panchang?.paksha, lang, "paksha")),
    row(labels[lang].vaar, translateByCategory(data.panchang?.vaar, lang, "weekday")),
    row(labels[lang].nakshatra, translateByCategory(data.panchang?.nakshatra, lang, "nakshatra")),
    row(labels[lang].yoga, translateByCategory(data.panchang?.yoga, lang, "yoga")),
    row(labels[lang].karan, translateByCategory(data.panchang?.karan, lang, "karan"))
