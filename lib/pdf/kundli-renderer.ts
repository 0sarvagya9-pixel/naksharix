function formatDateRange(start: unknown, end: unknown, lang: Locale) {
  const startText = safeText(start, lang);
  const endText = safeText(end, lang);
  if (isUnavailableText(startText) && isUnavailableText(endText)) return labels[lang].dashaUnavailable;
  if (isUnavailableText(startText)) return endText;
  if (isUnavailableText(endText)) return startText;
  return `${startText} - ${endText}`;
}

function safeInsight(value: unknown, lang: Locale, section: InsightSection) {
  const text = safeText(value, lang);
  if (!isUnavailableText(text) && !looksEnglishOnlyFallback(text, lang) && !(lang === "hi" && containsLatinProse(text))) return text;
  return insightFallbacks[lang][section];
}

function normalizeDisplayText(value: string, lang: Locale) {
  const text = value.trim().normalize("NFC");
  if (!text || isUnavailableText(text) || isRawOutput(text)) return labels[lang].notAvailable;
  const unavailableWords = "(?:Not available|Available nahi hai|उपलब्ध नहीं|null|undefined|\\[object Object\\])";
  const repeatedFallback = new RegExp(`^(?:${unavailableWords})(?:\\s*(?:-|,|/)\\s*${unavailableWords}|\\s+${unavailableWords})+$`, "i");
  if (repeatedFallback.test(text)) return labels[lang].notAvailable;
  return text.replace(new RegExp(`${unavailableWords}(?:\\s+${unavailableWords})+`, "gi"), labels[lang].notAvailable);
}

function isUnavailableText(value: string) {
  return /^(?:not available|available nahi hai|उपलब्ध नहीं|null|undefined|\[object Object\])$/i.test(value.trim());
}

function isRawOutput(value: string) {
  const text = value.trim();
  return (text.startsWith("{") && text.endsWith("}")) || (text.startsWith("[") && text.endsWith("]")) || text.includes("[object Object]");
}

function looksEnglishOnlyFallback(value: string, lang: Locale) {
  if (lang === "en") return false;
  return /not available|currently unavailable|review the chart|AI interpretation is currently limited/i.test(value);
}

function containsLatinProse(value: string) {
  return /[A-Za-z]{4,}/.test(value);
}

