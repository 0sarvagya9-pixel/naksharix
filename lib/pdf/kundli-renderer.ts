function requirePdfReact() {
  const getBuiltinModule = (process as typeof process & {
    getBuiltinModule?: (name: string) => typeof import("node:module");
  }).getBuiltinModule;
  const nodeModule = getBuiltinModule?.("module");
  const externalRequire = nodeModule?.createRequire(`${process.cwd()}/package.json`);
  if (!externalRequire) throw new Error("Node module loader unavailable for PDF renderer");
  return externalRequire("react") as typeof import("react");
}
