function safeText(value: unknown, lang: Locale): string {
  if (value === null || value === undefined || value === "") return labels[lang].notAvailable;
  if (typeof value === "string" || typeof value === "number" || typeof value === "boolean") return normalizeDisplayText(String(value), lang);
  if (Array.isArray(value)) {
    const items = value.map((item) => safeText(item, lang)).filter((item) => !isUnavailableText(item));
    return items.length ? items.join(", ") : labels[lang].notAvailable;
  }
  if (typeof value === "object") {
    const obj = value as Record<string, unknown>;
    if ("name" in obj) return safeText(obj.name, lang);
    if ("label" in obj) return safeText(obj.label, lang);
    if ("value" in obj) return safeText(obj.value, lang);
    if ("sign" in obj) return safeText(obj.sign, lang);
    if ("house" in obj) return safeText(obj.house, lang);
    if ("planet" in obj) return safeText(obj.planet, lang);
    return labels[lang].notAvailable;
  }
  return normalizeDisplayText(String(value), lang);
}

