import fs from "node:fs";
import path from "node:path";
import {
  planetExpectedMap,
  providerChart,
  providerMetadata,
  providerPanchang,
  providerTransit,
  providerVargaFromPlanets
} from "../lib/astrology/testing/provider-calculations.mjs";

const root = process.cwd();
const outDir = path.join(root, "fixtures", "generated");

const kundliSamples = [
  { id: "provider-kundli-delhi-1990", date: "1990-01-01", time: "12:00:00", timezone: "+05:30", latitude: 28.6139, longitude: 77.209, place: "Delhi, India" },
  { id: "provider-kundli-mumbai-2001", date: "2001-03-21", time: "23:45:00", timezone: "+05:30", latitude: 19.076, longitude: 72.8777, place: "Mumbai, India" },
  { id: "provider-kundli-new-york-1999", date: "1999-11-30", time: "18:15:00", timezone: "-05:00", latitude: 40.7128, longitude: -74.006, place: "New York, USA" },
  { id: "provider-kundli-london-1988", date: "1988-09-12", time: "09:20:00", timezone: "+01:00", latitude: 51.5074, longitude: -0.1278, place: "London, UK" },
  { id: "provider-kundli-sydney-1976", date: "1976-05-05", time: "03:05:00", timezone: "+10:00", latitude: -33.8688, longitude: 151.2093, place: "Sydney, Australia" }
];

const panchangSamples = [
  { id: "provider-panchang-delhi-2026-01-15", date: "2026-01-15", timezone: "+05:30", latitude: 28.6139, longitude: 77.209, place: "Delhi, India" },
  { id: "provider-panchang-mumbai-2026-08-10", date: "2026-08-10", timezone: "+05:30", latitude: 19.076, longitude: 72.8777, place: "Mumbai, India" },
  { id: "provider-panchang-new-york-2026-04-20", date: "2026-04-20", timezone: "-04:00", latitude: 40.7128, longitude: -74.006, place: "New York, USA" }
];

const transitSamples = [
  { id: "provider-transit-delhi-2026-01-15", date: "2026-01-15", time: "12:00:00", timezone: "+05:30", latitude: 28.6139, longitude: 77.209, place: "Delhi, India" },
  { id: "provider-transit-mumbai-2026-08-10", date: "2026-08-10", time: "12:00:00", timezone: "+05:30", latitude: 19.076, longitude: 72.8777, place: "Mumbai, India" },
  { id: "provider-transit-new-york-2026-04-20", date: "2026-04-20", time: "12:00:00", timezone: "-04:00", latitude: 40.7128, longitude: -74.006, place: "New York, USA" }
];

await fs.promises.mkdir(outDir, { recursive: true });

const kundliFixtures = [];
for (const sample of kundliSamples) {
  const chart = await providerChart({ ...sample, ayanamsa: "lahiri", house_system: "Vedic Whole Sign" });
  kundliFixtures.push({
    id: sample.id,
    source: providerMetadata.providerName,
    provider_metadata: providerMetadata,
    source_note: "Generated by Naksharix's deterministic local astronomy-engine provider. This is provider_verified regression data, not external Swiss/Jagannatha Hora verification.",
    verification_level: "provider_verified",
    external_verification: false,
    input: { ...sample, ayanamsa: "lahiri", house_system: "Vedic Whole Sign" },
    expected: {
      ascendant: { ...chart.ascendant, toleranceDegrees: 0.000001 },
      planets: planetExpectedMap(chart.planets),
      moon: {
        ...chart.moon,
        nakshatra: chart.nakshatra.name,
        pada: chart.nakshatra.pada,
        toleranceDegrees: 0.000001
      },
      dasha: {
        startingMahadashaLord: chart.dasha?.startingMahadashaLord ?? null,
        balanceAtBirth: chart.dasha?.balanceAtBirth ?? null,
        expectedTransitions: chart.dasha?.transitions ?? [],
        toleranceYears: 0.000001
      }
    }
  });
}

const panchangFixtures = panchangSamples.map((sample) => {
  const panchang = providerPanchang(sample);
  return {
    id: sample.id,
    source: providerMetadata.providerName,
    provider_metadata: providerMetadata,
    source_note: "Generated by Naksharix's deterministic local astronomy-engine Panchang provider. This is provider_verified regression data, not external Panchang validation.",
    verification_level: "provider_verified",
    external_verification: false,
    input: sample,
    expected: panchang
  };
});

const transitFixtures = [];
for (const sample of transitSamples) {
  const transit = await providerTransit({ ...sample, ayanamsa: "lahiri" });
  transitFixtures.push({
    id: sample.id,
    source: providerMetadata.providerName,
    provider_metadata: providerMetadata,
    source_note: "Generated by Naksharix's deterministic local astronomy-engine transit snapshot provider. Ingress/retrograde windows are not included.",
    verification_level: "provider_verified",
    external_verification: false,
    input: { ...sample, ayanamsa: "lahiri" },
    expected: transit
  });
}

const vargaFixtures = kundliFixtures.map((fixture) => {
  const planets = Object.entries(fixture.expected.planets).map(([planet, value]) => ({ planet, ...value }));
  return {
    id: `provider-varga-${fixture.id.replace("provider-kundli-", "")}`,
    source: providerMetadata.providerName,
    provider_metadata: providerMetadata,
    source_note: "Generated from provider Kundli sidereal planet longitudes using Naksharix Varga formulas. Provider regression only.",
    verification_level: "provider_verified",
    external_verification: false,
    input: {
      kundliFixtureId: fixture.id,
      planets
    },
    expected: {
      charts: providerVargaFromPlanets(planets)
    }
  };
});

await writeJson("provider-kundli-fixtures.json", kundliFixtures);
await writeJson("provider-panchang-fixtures.json", panchangFixtures);
await writeJson("provider-transit-fixtures.json", transitFixtures);
await writeJson("provider-varga-fixtures.json", vargaFixtures);

console.log(`Generated provider fixtures in ${path.relative(root, outDir)}`);
console.log(`Kundli: ${kundliFixtures.length}, Panchang: ${panchangFixtures.length}, Transit: ${transitFixtures.length}, Varga: ${vargaFixtures.length}`);

async function writeJson(fileName, data) {
  await fs.promises.writeFile(path.join(outDir, fileName), `${JSON.stringify(data, null, 2)}\n`, "utf8");
}
